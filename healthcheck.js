 const http = require('http');  // Use 'https' if your API is served over HTTPS

const PORT = process.env.PORT || 3000;

const options = {
  hostname: 'localhost',
  port: PORT, // Use 443 if using HTTPS
  path: '/health',
  method: 'GET',
  timeout: 5000, // 5 seconds timeout
};

const req = http.request(options, (res) => {
  if (res.statusCode === 200) {
    process.exit(0);
  } else {
    process.exit(1);
  }
});

req.on('error', (error) => {
  console.error(error);
  process.exit(1);
});

req.on('timeout', () => {
  console.error('Request timed out');
  req.destroy();
  process.exit(1);
});

req.end();
